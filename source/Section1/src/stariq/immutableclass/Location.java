package stariq.immutableclass;

// Challenge given:
// Work out what is wrong with the Location constructor
// that will allow the program to compile but crash on runtime.
// Once the problem is identified, modify the code to fix it.
// Issue found: If a null value is passed instead of a map as exits parameter,
// the program crashes because of NullPointerException.

import java.util.HashMap;
import java.util.Map;

public class Location {

    private final int locationID;
    private final String description;
    private final Map<String, Integer> exits;

    public Location(int locationID, String description, Map<String, Integer> exits) {
        this.locationID = locationID;
        this.description = description;
        if(exits != null) {
            this.exits = new HashMap<>(exits);
        } else {
            this.exits = new HashMap<>();
        }
        this.exits.put("Q", 0);
    }

    public int getLocationID() {
        return locationID;
    }

    public String getDescription() {
        return description;
    }

    public Map<String, Integer> getExits() {
        return new HashMap<String, Integer>(exits);
    }
}
